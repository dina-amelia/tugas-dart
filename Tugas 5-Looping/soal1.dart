void main() {
  print('LOOPING PERTAMA');
  var flag = 2;
  while (flag <= 21) {
    print(flag.toString() + ' - I love coding');
    flag += 2;
  }

  print('\n LOOPING KEDUA');
  var fleg = 20;
  while (fleg >= 2) {
    print(fleg.toString() + ' - I will become a mobile developer');
    fleg -= 2;
  }
}
